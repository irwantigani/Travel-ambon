      <h1 class="entry-title">Benteng Duurstede</h1>
      <p style="text-align: justify;">Benteng Duurstede adalah suatu benteng Belanda abad ke-17 yang berlokasi di Saparua, Maluku, Indonesia.[1][2] Benteng ini awalnya melindungi Desa Saparoea. Panduan navigasi dari tahun 1878 menyatakan: "terdapat pelabuhan yang bagus di dekat benteng ini di musim angin barat sekitar 12 fathom."
      Benteng ini terbuka untuk umum dan telah banyak dipulihkan.
      Benteng Duurstede dibangun pertama kali oleh Portugis pada tahun 1676, kemudian direbut, dimanfaatkan dan dibangun kembali oleh Gubernur Ambon Mr. Nicolaas Schaghen pada tahun 1691. Benteng Duurstede berfungsi sebagai bangunan pertahanan serta pusat pemerintahan Perusahaan Hindia Timur Belanda (Vereenigde Oostindische Compagnie; VOC) 
      selama menguasai wilayah Saparua. Pada 16 Mei 1817 benteng ini diserbu oleh rakyat Saparua di bawah pimpinan Kapitan Pattimura, seluruh penghuni benteng tewas kecuali putra residen yang bernama Juan Van Den Berg. Jatuhnya benteng Duurstede di tangan rakyat Maluku mengakibatkan kedudukan VOC di Ambon dan Batavia goncang. Oleh karena itu, 
      VOC memusatkan perhatiannya untuk merebut kembali benteng. Segala usaha telah dilakukan VOC di antaranya adalah mengirim bantuan tentara dan persenjataan perang, namun demikian setiap penyerangan tersebut selalu gagal. Situasi ini mendorong VOC bertindak lebih agresif, Komisaris van Middelkoop terpaksa meminta bantuan kepada Raja Ternate dan Tidore.
      Pada bulan November 1817, VOC mengirimkan armada yang berjumlah 1500 orang atas sumbangan dari Raja Ternate dan Tidore tentunya. Penyerbuan ini dipimpin oleh Komisaris Jendral A. A Buyskers. Strategi yang dilakukan oleh Buyskers adalah menguasai pulau-pulau di sekitar Saparua, dan selanjutnya menguasai daerah kekuasaan Pattimura. Strategi tersebut ternyata cukup berhasil, 
      Pattimura beserta pasukannya terdesak ke hutan sagu dan pegunungan, hingga akhirnya Kapitan Pattimura beserta tiga orang panglima berhasil ditangkap. Mereka dijatuhi hukuman mati yang dilaksanakan di Benteng Nieuw Victoria.
