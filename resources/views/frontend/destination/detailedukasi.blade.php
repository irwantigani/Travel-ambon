<p style="text-align: justify;">Museum Siwalima terlak di kawasan Taman Makmur, Desa Amahusu, Kecamatan Nusaniwe, Kota Ambon, Propinsi Maluku. Museum ini didirikan pada tanggal 8 November 1973, namun diresmikan pada tanggal 26 Maret 1977. Walaupun obyek wisata yang satu ini merupakan tempat peninggalan benda-benda bersejarah, namun bangunan museum ini berada di atas bukit yang menghadap Teluk Ambon dengan keeksotisannya yang akan membuatmu terpesona.
